
class Calc:
    @staticmethod
    def multiplies(a, b):
        return a*b

    @staticmethod
    def divides(a, b):
        return a/b

    @staticmethod
    def adds(a, b):
        return a+b

    @staticmethod
    def subtracts(a, b):
        return a-b

    @staticmethod
    def exponentiates(a, b):
        return pow(a, b)
